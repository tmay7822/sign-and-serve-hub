// Helper functions
export const getLocationPageBySlug = (slug: string): LocationPageData | undefined => {
  return LOCATION_PAGES.find(page => page.slug === slug);
};

export const getLocationPageByPath = (path: string): LocationPageData | undefined => {
  // Normalize path to ensure it starts with /
  const normalizedPath = path.startsWith('/') ? path : `/${path}`;
  return LOCATION_PAGES.find(page => page.path === normalizedPath);
};

export const getAllLocationSlugs = (): string[] => {
  return LOCATION_PAGES.map(page => page.slug);
};

